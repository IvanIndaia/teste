
public class Ex1_Calculadora {

    public static void soma(double num1, double num2){
            double result = num1+num2;
        System.out.println("A soma de "+ num1 + " + " + num2+ " = "+ result);
    }
    public static void multi(double num1, double num2){
        double result = num1*num2;
        System.out.println("A soma de "+ num1 + " * " + num2+ " = "+ result);
    }
    public static void subt(double num1, double num2){
        double result = num1-num2;
        System.out.println("A soma de "+ num1 + " - " + num2+ " = "+ result);
    }
    public static void divi(double num1, double num2){
        double result = num1/num2;
        System.out.println("A soma de "+ num1 + " / " + num2+ " = "+ result);
    }

}
